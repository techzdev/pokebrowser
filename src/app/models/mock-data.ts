// Generate more mock data for pagination simulation
export function generateMockPokemon(offset: number, limit: number) {
  const results: typeof MOCK_POKEMON_DATA = [];
  
  for (let i = 0; i < limit; i++) {
    const id = offset + i + 1;
    if (id > 1008) break; // Pokemon limit
    
    // Use existing data if available, otherwise generate
    const existing = MOCK_POKEMON_DATA.find(p => p.id === id);
    if (existing) {
      results.push(existing);
    } else {
      results.push({
        id,
        name: `pokemon-${id}`,
        url: `https://pokeapi.co/api/v2/pokemon/${id}/`,
        imageUrl: `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/${id}.png`,
        types: [
          { slot: 1, type: { name: 'normal', url: 'https://pokeapi.co/api/v2/type/1/' } }
        ],
        height: Math.floor(Math.random() * 20) + 5,
        weight: Math.floor(Math.random() * 1000) + 50
      });
    }
  }
  
  return results;
}